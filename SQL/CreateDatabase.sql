/*==============================================================*/
/* TABLE: CREAT                                                 */
/*==============================================================*/
CREATE TABLE ALBUM_CREATED_BY (
  USER_ID INTEGER NOT NULL,
  ALBUM_ID INTEGER NOT NULL,
  PRIMARY KEY (USER_ID, ALBUM_ID)
);

/*==============================================================*/
/* TABLE: FOLLOW                                                */
/*==============================================================*/
CREATE TABLE FOLLOW (
    USER_ID INTEGER NOT NULL,
    USER_FOLLOW_BY_ID INTEGER NOT NULL,
    PRIMARY KEY (USER_ID, USER_FOLLOW_BY_ID)
);

/*==============================================================*/
/* TABLE: HISTORY                                               */
/*==============================================================*/
CREATE TABLE HISTORY (
    LISTEN_DATE DATETIME NOT NULL,
    USER_ID INTEGER NOT NULL,
    MUSIC_ID INTEGER NOT NULL,
    PRIMARY KEY (LISTEN_DATE, USER_ID, MUSIC_ID)
);

/*==============================================================*/
/* TABLE: MUSIC                                                 */
/*==============================================================*/
CREATE TABLE SONG (
    SONG_ID INTEGER NOT NULL,
    SONG_NAME NVARCHAR(255),
    SONG_IMG NVARCHAR(255) NOT NULL,
    TOTAL_VIEW INTEGER,
    DURATION INTEGER,
    SONG_LOCATION NVARCHAR(255),
    LYRICS JSON,
    ADDED_DATE DATETIME,
    PRIMARY KEY (SONG_ID)
);

/*==============================================================*/
/* TABLE: MUSIC_ALBUM                                           */
/*==============================================================*/
CREATE TABLE SONG_ALBUM (
     SONG_ID INTEGER NOT NULL,
     ALBUM_ID INTEGER NOT NULL,
     PRIMARY KEY (SONG_ID, ALBUM_ID)
);

/*==============================================================*/
/* TABLE: RECEIPT                                               */
/*==============================================================*/
CREATE TABLE RECEIPT (
    ID_RECEIPT NVARCHAR(255) NOT NULL,
    ID_USER INTEGER NOT NULL,
    DATE_BUY DATE,
    TOTAL_PRICE INTEGER,
    PAYMENT NVARCHAR(255),
    RETAILER NVARCHAR(255),
    ADDRESS NVARCHAR(255),
    VAT_NUMBER INTEGER,
    PRODUCT NVARCHAR(255),
    TOTAL_TAX INTEGER,
    TAX INTEGER,
    PRICE INTEGER,
    PRIMARY KEY (ID_RECEIPT)
);

/*==============================================================*/
/* TABLE: SING_BY                                                  */
/*==============================================================*/
CREATE TABLE SING_BY (
     AUTHOR_ID INTEGER NOT NULL,
     MUSIC_ID INTEGER NOT NULL,
     PRIMARY KEY (AUTHOR_ID, MUSIC_ID)
);


/*==============================================================*/
/* TABLE: USER                                                */
/*==============================================================*/
CREATE TABLE USER (
      USER_ID INTEGER NOT NULL,
      NAME NVARCHAR(255),
      PASSWORD NVARCHAR(255),
      GENDER BOOLEAN,
      BIRTH DATE,
      VERIFY BOOLEAN,
      COUNTRY NVARCHAR(255),
      EMAIL NVARCHAR(255),
      HAVE_PREMIUM BOOLEAN,
      TYPE INTEGER,
      MONTHLY_LISTENER INTEGER,
      PRIMARY KEY (USER_ID)
);

